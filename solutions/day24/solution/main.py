def MONAD_v2(i):

    z = 0
    i = list(map(int, list(i)))

    # Step 1
    z = z * 26 + i[0] + 6

    # Step 2
    z = z * 26 + i[1] + 6

    # Step 3
    z = z * 26 + i[2] + 3

    # Step 4
    if i[3] == z % 26 - 11:
        z = z // 26
    else:
        z = (z // 26) * 26 + i[3] + 11

    # Step 5
    z = z * 26 + i[4] + 9

    # Step 6
    if i[5] == z % 26 - 1:
        z = z // 26
    else:
        z = (z // 26) * 26 + i[5] + 3

    # Step 7
    z = z * 26 + i[6] + 13

    # Step 8
    z = z * 26 + i[7] + 6

    # Step 9
    if i[8] == z % 26:
        z = z // 26
    else:
        z = (z // 26) * 26 + i[8] + 14

    # Step 10
    z = z * 26 + i[9] + 10

    # Step 11
    if i[10] == z % 26 - 5:
        z = z // 26
    else:
        z = (z // 26) * 26 + i[10] + 12

    # Step 12
    if i[11] == z % 26 - 16:
        z = z // 26
    else:
        z = (z // 26) * 26 + i[11] + 10

    # Step 13
    if i[12] == z % 26 - 7:
        z = z // 26
    else:
        z = (z // 26) * 26 + i[12] + 11

    # Step 14
    if i[13] == z % 26 - 11:
        z = z // 26
    else:
        z = (z // 26) * 26 + i[13] + 15

    return z


