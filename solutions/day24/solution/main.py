def generate_random_input():
    """Generate a sequence of fourteen digits between 1 and 9"""

    candidates = list('123456789')
    return ''.join(random.choices(candidates, k=14))


# ====================
def test_v1_v2_equal():
    """Test that the results of MONAD_V1 and MONAD_v2 are equal using 10
    random inputs"""

    print("=== test_v1_v2_equal ===")
    for _ in range(10):
        input_ = generate_random_input()
        m1_result = MONAD_v1(input_)
        m2_result = MONAD_v2(input_)
        try:
            assert m1_result == m2_result
            print("Test passed.")
        except AssertionError:
            print("!!! Test failed for input:", input_, "!!!")
            print("MONAD 1 gives:", m1_result)
            print("MONAD 2 gives:", m2_result)
            quit()


